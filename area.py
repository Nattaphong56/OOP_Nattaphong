print("โปรแกรมคำนวณหาพื้นที่ 4 เหลี่ยมจตุรัส")
Side = int(input("ใส่ความยาวด้าน : "))
sum = Side *  Side
print(f'ค่าพื้นที่ 4 เหลี่ยมจตุรัส = {sum}\n')

print("โปรแกรมคำนวณหาพื้นที่ 4 เหลี่ยมผืนผ้า")
Width = int(input("ใส่ความกว้าง : "))
length = int(input("ใส่ความยาว : "))
sum = Width * length
print(f'ค่าพื้นที่ 4 เหลี่ยมผืนผ้า = {sum}\n')

